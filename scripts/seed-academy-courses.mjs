let blockSequence = 0;

const escapeHtml = (value) => String(value ?? '')
  .replaceAll('&', '&amp;')
  .replaceAll('<', '&lt;')
  .replaceAll('>', '&gt;')
  .replaceAll('"', '&quot;')
  .replaceAll("'", '&#039;');

const block = (type, data) => ({
  id: `seed-block-${String(++blockSequence).padStart(3, '0')}`,
  type,
  data,
});

const paragraphList = (items = []) => items.map((item) => `<p>${escapeHtml(item)}</p>`).join('');
const bulletList = (items = []) => items.length
  ? `<ul>${items.map((item) => `<li>${escapeHtml(item)}</li>`).join('')}</ul>`
  : '';

const lessonBlocks = (lesson, includeContact = false) => {
  const sections = lesson.sections.map((section) => `
    <h2>${escapeHtml(section.title)}</h2>
    ${paragraphList(section.paragraphs)}
    ${bulletList(section.items)}
  `).join('');
  const practice = lesson.practice ? `
    <h2>${escapeHtml(lesson.practice.title)}</h2>
    <ol>${lesson.practice.steps.map((item) => `<li>${escapeHtml(item)}</li>`).join('')}</ol>
    ${lesson.practice.questions?.length ? `<h3>Вопросы для дневника</h3>${bulletList(lesson.practice.questions)}` : ''}
  ` : '';
  const result = [
    block('text', { html: `<p><strong>${escapeHtml(lesson.lead)}</strong></p>${sections}${practice}` }),
    block('quote', { text: lesson.quote, author: lesson.quoteAuthor || 'Практика Атмики' }),
  ];
  if (lesson.closing) result.push(block('text', { html: `<h2>Завершение урока</h2><p>${escapeHtml(lesson.closing)}</p>` }));
  if (includeContact) result.push(block('button', { label: 'Связаться и согласовать следующий шаг', url: contactUrl, style: 'outline' }));
  return result;
};

